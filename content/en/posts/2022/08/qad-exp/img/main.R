figure_qad <- function() {
  p <- seq(0, 1, by = 0.001)
  qad <- ifelse(p <= 0.75, log(p + sqrt(p^2 + 1)), -log(2) - log(1 - p))
  df <- data.frame(p, qad)
  ggplot(df, aes(p, qad)) +
    geom_line() +
    labs(y = "QAD(X, p)", title = "QAD of the Exponential distribution")
}

