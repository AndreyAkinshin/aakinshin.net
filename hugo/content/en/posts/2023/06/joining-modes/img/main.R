figure_pair <- function() {
  set.seed(1729)
  n <- 10000
  x <- ifelse(sample(0:1, n, T), runif(n), runif(n, 1.05, 2.05))
  ggplot(data.frame(x), aes(x)) +
    geom_density(bw = "SJ") +
    geom_rug()
}

