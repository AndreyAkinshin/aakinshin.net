figure_qad <- function() {
  p <- seq(0, 1, by = 0.001)
  qad <- ifelse(p <= 2 / 3, -1 / p + sqrt(1 / p^2 + 4), (2 * p - 1) / (1 - p))
  df <- data.frame(p, qad)
  ggplot(df, aes(p, qad)) +
    geom_line() +
    labs(y = "QAD(X, p)", title = "QAD of the Pareto(1,1) distribution")
}

